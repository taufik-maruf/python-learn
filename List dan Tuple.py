# Mengakses List dan Tuple
bulan_pembelian = ('Januari','Febuari','Maret','April','Mei','Juni','Juli','Agustus','September','Oktober', 'November','Desember')

print(bulan_pembelian[0])	# Menampilkan Bulan Januari
print(bulan_pembelian[5])	# Menampilkan Bulan Juni

# Dapat juga menggunakan indeks negatif
print(bulan_pembelian[-1])	# Menampilkan Bulan Desember
print(bulan_pembelian[-2])	# Menampilkan Bulan November
