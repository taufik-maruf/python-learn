# ELSE --> jika program tidak sesuai maka akan dijalankan

light_on = True
if light_on:
  print('It\'s  bright here')      # Jika True akan dieksekusi
else:
  print('Oh no, it\'s dark here')  # Jika False akan dieksekusi

# Comparison

age = 25
if age >= 18:      # Jika variabel 'age' lebih dari sama dengan 18 (Benar/True)
  print('adult')   # Program akan dieksekusi
else:
  print('child')   # Jika kurang dari 18 (False) Program akan dieksekusi

# Operasi yang bisa digunakan
# == --> sama dengan (jangan menggunakan = karena itu adalah operator assigment, bukan comparator)
# != --> tidak sama dengan
# >= --> lebih dari sama dengan
# <= --> kurang dari sama dengan
# >  --> lebih dari
# <  --> kurang dari
